package EjercicioTarea;

public class Ejercicio03 {
    public static void main(String[] args) {
        //  Ejercicio 03 => Tabla de multiplicar
        short nBase = 5;
        short nLimit = 10;
        for (int i = 1; i <= nLimit ; i++) {
            System.out.println(nBase + " X " + i + " = " + (i * nBase));
        }
    }
}
