package EjercicioTarea;

public class Ejercicio01 {
    public static void main(String[] args) {
        //  Ejercicio 01 => Contador de número pares
        for (int i = 1; i <= 50; i++) {
            if (i % 2 == 0) {
                System.out.println("El número " + i + " es par");
            }
        }
    }
}
